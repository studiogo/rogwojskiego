    <h3>Social Login</h3>
    <p> <b>Ultimate Social Login</b> - is a perfect WordPress plugin to allow your website users to register/login to the website using one of their favorite social website accounts.</p>

    <p>As a website owner you can easily configure which social accounts to enable/disable for login, which areas of the website social login should be displayed (login, registration, comments, checkout) and other various configuration including text/strings configuration. Also you can select the social login buttons/icons from 15 beautifully designed templates to fit them with your website’s overall design/theme. Your website will fetch real data of the user’s social profile!</p>

    <p>Your website visitors, can choose one of their favorite social accounts to login into your website. No need to fill up the long registration forms and remember many login credentials.</p>

    <h4>Plugin setup is as easy as 1, 2, 3! </h4>
    <ol>
        <li><b>Select from the most popular 9 social media</b><br/>
           - Configure which social media option to display at login area.</li>
        <li><b>Easy setup </b><br>- Just put a few authorization details for each option you want to enable. Select which login area you'd like to show the social login option ( login, registration, comments etc).</li>
        <li><b>Beautify your login page</b><br/>- Select from 16 beautiful designs of social login buttons/icons.</li>
    </ol>
    <p>Your website visitors can now login using one of their favorite social account login.  Easy, quick and convenient.</p>

   

